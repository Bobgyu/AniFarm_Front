// 파종
// 파종상의 온도는 20℃전후가 적당한데 저온에서는 발아가 늦어지고 30℃이상의 고온에서는 발아율이 떨어지며, 초기생육에도 지장이 많다. 따라서, 저온시에는 반드시 온상을 설치해서 파종해야 발아가 일정하고 생육상태가 좋아서 우량묘를 육성할 수 있으며 평균기온이 15℃ 이상일 때는 냉상을 설치해서 파종한다.

// 파종량은 10a당 약 2㎗로서 실면적 10평 정도면 충분하다. 파종이 끝나면 판자로 종자를 약간 누른 후 상토를 아주 가볍게 덮고 판자로 복토위를 좌우로 이동시켜 복토두께를 고르게 한 다음 볏짚으로 약간 피복하고 저온기에는 미온수로 관수하고 고온기에는 샘물을 주어서 지온을 낮추어주면 발아율이 더욱 좋아진다.
// 가식
// 1차 가식은 본엽 1.5~2매시 6×3~4Cm로 실시하는데, 저온기에는 1차 가식까지가 30~35일 걸리고 고온기에는 15~20일 소요된다. 육묘상이 너무 과습하거나 건조하면 엽고병이 발생하기 쉬우므로 다이센M-45를 400배로 희석해서 2~3차례 살포해주고 주간온도를 너무 높이지 말고 평균 15~20℃를 유지하는 것이 좋다.
// 정식
// 본엽이 5~7장 전개되었을 때 정식하는데, 터널재배를 할 경우에는 120Cm 정도의 이랑에 5~6줄을 심으며, 배수가 나쁜 점질토양에서는 이랑을 높게 하여 70Cm정도의 이랑에 2줄씩 심는다. 포기사이는 잎상추가 25Cm, 결구상추는 30Cm가 적당하다.
// 상추는 실뿌리는 많으나 약한 편이므로 뿌리를 많이 붙여 정성들여 취급하여야 정식후의 회복이 빠르다.
// 시비
// 상추는 생육기간이 짧고 뿌리도 잘 발달되지 않으므로 밑거름 중심으로 충분히 주어야 하는데, 생식을 하는 채소이므로 완숙한 퇴비만을 시용하도록 하고 특히 인분뇨의 사용은 절대 금하여야 한다.
// 시비량은 잎상추에 있어서는 10a당 완숙퇴비 2,000kg과 질소:인산:칼리는 성분량으로 20:10:15kg 정도를 표준으로 한다. 점질토양에서는 양토 또는 사질양토에서보다 시비량을 줄이는데, 특히 질소와 칼리성분을 약 절 반 정도로 줄여서 시비토록 한다.

// 추비는 정식후 15일경 뿌리가 완전히 활착된 후부터 수확전 20일경까지 10a당 3~5kg의 요소를 2~3회에 걸쳐 시비하고, 수확전 10~15일경부터는 요소를 0.5%정도로 물에 타서 2~3일 간격으로 약 3회 살포하여 주면 잎의 색깔이 진해지고 상품가치가 높아진다.

// 결구상추는 잎상추보다 다소 많은 양의 거름을 필요로 하며, 배추나 양배추와 같이 인산질이 부족하면 결구가 잘 되지 않으며, 칼리질이 부족하면 불완전 결구가 되므로 질소질비료 외에 인산과 칼리를 반드시 함께 시용하도록 한다.
// 수확 및 저장
// 수확
// 잎상추는 정식후 30일경부터는 수확이 가능하며 정식한 묘가 활착되어 왕성한 생육을 보이기 시작하면 겉잎부터 차례로 뜯어 수확하거나 큰것부터 솎아서 수확한다.
// 결구상추도 정식 후 45~50일경부터는 수확기에 도달하는데 결구한 것부터 차례로 수확한다.
// 10a당 수량은 잎상추는 약 3,000kg, 결구상추는 2,000~3,000kg정도이나 작형 및 수확기에 따라서 달라질 수 있다.
// 저장
// 잎상추는 거의 저장이 되지 않으나, 결구상추의 경우에는 최근 미국등 몇 나라에서 저장기술이 크게 개발되었다. 즉 포장에서 수확직후 상자에 넣어 감압상태하에서 냉각시켜 냉동시설을 갖춘 트럭에 실어 장거리 수송을 한 후 소비지 근처에서 계속 저온다습(1~2℃, 상대습도 95%)한 상태로 저장을 하면 수확 후 20일까지는 시판에 지장이 없을 정도의 품질을 유지할 수 있다고 한다.
// 현재 우리 나라에서 실용화되고 있는 방법으로는 수확한 결구상추를 크기별로 선발하여 자체접착성이 있는 얇은 플라스틱 필름으로 개개의 결구상추를 포장하는 것이 있다. 이 방법은 상추가 돋보이고 시드는 것을 방지함과 동시에 운반과 취급에 따른 기계적인 상처를 줄일 수 있으므로 효과적이다.

