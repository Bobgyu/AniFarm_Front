// 3. 왜 main의 크기가 바뀌는가?
// Flexbox 레이아웃에서는 자식 요소들의 크기나 배치가 부모 요소의 크기에 영향을 미칠 수 있습니다.

// img의 크기를 w-[10%]로 설정하면, img의 너비는 main의 너비의 10%에 맞춰집니다. 이때 main은 flexbox 레이아웃을 사용하고 있기 때문에, 자식 요소인 img의 크기가 변경되면 자식 요소의 크기에 맞춰서 부모 요소(main)가 재조정될 수 있습니다.

// 수직 및 수평 중앙 정렬이 적용되었기 때문에, img의 크기에 따라 main의 배치가 영향을 받을 수 있습니다. 즉, img가 main 안에서 수평과 수직으로 정확히 중앙에 위치하도록 설정되어 있기 때문에, img의 크기가 바뀌면 main의 크기도 이에 맞춰 변할 수 있습니다.

// 기본적으로 Flexbox는 자식 요소의 크기를 기반으로 부모 요소의 크기를 자동으로 결정합니다. main의 크기는 ma.
// 예시:

// jsx
// 복사
// <main className="w-screen h-screen flex justify-center items-center">
//   <img className="w-[10%] h-auto" src={apple} alt="apple" />
// </main>
// 이렇게 main의 크기를 고정해주면, 자식 요소인 img가 크기 변경을 해도 main의 크기가 변하지 않게 됩니다.

// 결론
// main의 크기가 바뀌는 이유는 Flexbox의 특성상 자식 요소의 크기가 부모 요소에 영향을 미치기 때문입니다. main에 고정된 크기를 지정하거나 img의 크기를 조정하여 부모 요소의 크기를 제한할 수 있습니다.in 안에 포함된 요소들의 크기에 따라 조정되며, 특히 자식 요소인 img가 크기를 차지하게 되면 main의 크기도 영향을 받게 됩니다.

// 해결 방법
// main의 크기를 고정하려면 main에 명시적인 너비와 높이를 설정해 줄 수 있습니다. 예를 들어, w-full과 h-full 대신에 고정된 크기를 설정하여 main이 자식 요소에 의존하지 않도록 할 수 있습니다
